# Tipos de dato por referencia.

my_list_a = [10, 20]
my_list_b = my_list_a
my_list_b.append(30)
print(my_list_a)
print(my_list_b) # Los datos por referencia no copian el valor, lo que hacen es copiar su dirección de la memoria.
